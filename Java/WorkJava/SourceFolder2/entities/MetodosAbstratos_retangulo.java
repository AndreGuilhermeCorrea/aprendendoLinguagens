package entities;

import entidade_enums.MetodosAbstratos_Color;

public class MetodosAbstratos_retangulo extends MetodosAbstratos_shape {

	private Double width;
	private Double height;

	public MetodosAbstratos_retangulo() {
		super();
	}

	public MetodosAbstratos_retangulo(MetodosAbstratos_Color metodosAbstratos_Color, Double width, Double height) {
		super(metodosAbstratos_Color);
		this.width = width;
		this.height = height;
	}

	public Double getWidth() {
		return width;
	}

	public void setWidth(Double width) {
		this.width = width;
	}

	public Double getHeight() {
		return height;
	}

	public void setHeight(Double height) {
		this.height = height;
	}
	//area do retangulo
	@Override
	public double area() {
		return width * height;
	}
}